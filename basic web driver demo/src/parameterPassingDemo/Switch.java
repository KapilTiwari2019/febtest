package parameterPassingDemo;

public class Switch {
	public static void main(String[] args) {
		
		int x = 100;
		switch(80) {
		case 90:
			System.out.println("A+");
			break;
		case 80:
			System.out.println("A");
			break;
		case 70:
			System.out.println("B+");
			break;
			default:
				System.out.println("All in B");
		}
	}

}
